#include <iostream> // Preprocessor directive to include iostream library

// The `main` function is the entry point of the program
int main()
{

    // `using namespace std;` is included inside the main function to simplify the syntax
    // Without needing to prefix std:: before `cout` and `endl`.
    using namespace std;

    cout << "Pawan" << endl;  // Prints "Pawan" followed by a newline.
    cout << "Maurya" << endl; // Prints "Maurya" followed by a newline.
    cout << "Hey " << endl;   // Prints "Hey " followed by a newline.

    return 0; // Return 0 to indicate successful program execution
}
