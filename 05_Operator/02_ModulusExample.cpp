// Demonstrates the modulus operator with different numbers

#include <iostream>
using namespace std;

int main()
{
    // Declare two integer variables
    int num1 = 10;
    int num2 = 15;

    // Modulus operation
    cout << "num1 % num2 = " << num1 % num2 << endl; // Modulus: num1 divided by num2
}
